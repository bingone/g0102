package vo.BE;

public class voBEProduct {
	private String productNo;
	private String productName;
	private String packaging;
	private String productclass;
	private String specifiction;
	private String requality;
	public String getProductNo() {
		return productNo;
	}
	public void setProductNo(String productNo) {
		this.productNo = productNo;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getPackaging() {
		return packaging;
	}
	public void setPackaging(String packaging) {
		this.packaging = packaging;
	}
	public String getProductclass() {
		return productclass;
	}
	public void setProductclass(String productclass) {
		this.productclass = productclass;
	}
	public String getSpecifiction() {
		return specifiction;
	}
	public void setSpecifiction(String specifiction) {
		this.specifiction = specifiction;
	}
	public String getRequality() {
		return requality;
	}
	public void setRequality(String requality) {
		this.requality = requality;
	}
	
	
}
